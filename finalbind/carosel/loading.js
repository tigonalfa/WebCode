window.addEventListener('DOMContentLoaded', function () {
    document.getElementById("loading-screen").style.display = "none";
});

const popupButton = document.getElementById("popup-button");
const popupContainers = document.querySelectorAll(".popup-container");
const prevButton = document.getElementsByClassName("swiper-button-prev");
const nextButton = document.getElementsByClassName("swiper-button-next");

popupButton.addEventListener("click", () => {
    // Toggle the display of popup containers
    popupContainers.forEach(container => {
        container.style.display = "block"
    });
});

// Add a click event listener to the document
document.addEventListener("click", (event) => {
    // Check if the click target is outside of any of the popup containers
    let clickedOutside = true;
    popupContainers.forEach(container => {
        if (container.contains(event.target) || event.target.id === "popup-button" || event.target.id === "swiper-button-prev" || event.target.id === "swiper-button-next") {
            // Clicked inside the current container or on one of the buttons
            clickedOutside = false;
        }
    });

    if (clickedOutside) {
        // Close all pop-up containers
        popupContainers.forEach(container => {
            container.style.display = "none";
        });
    }
});

